<p>
he Tesla Model S is a great four-door luxury sports car that just also happens to be an electric vehicle. 
A large, low-slung hatchback, it easily seats five, or seven in a pinch when fitted with rear-facing jump seats. 
With its optional 85 kWh battery, the Tesla can travel between 180 and 225 miles per charge, at least twice as far as other electrics. 
It can also be fully charged in as little as five hours. Performance is exceptional, with thrilling acceleration, pinpoint handling, and a superb ride. 
A huge iPad-like center screen controls nearly all accessory functions
